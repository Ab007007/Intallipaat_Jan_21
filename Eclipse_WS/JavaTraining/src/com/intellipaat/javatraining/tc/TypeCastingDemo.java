package com.intellipaat.javatraining.tc;

public class TypeCastingDemo {
	
	
	public static void main(String[] args) {
		
		
		int a = 100;
		
		double a1 = a;
		
		System.out.println(a);
		System.out.println(a1);
		
		
		double d = 100.50;
		int d1 = (int)d;
		System.out.println(d);
		System.out.println(d1);
	}

}
